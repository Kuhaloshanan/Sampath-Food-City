        if (result.equals("success")) {
            JOptionPane.showMessageDialog(null, "Registration successful!");
            txt_name.setText("");
            txt_email.setText("");
            txt_user.setText("");
            psw_fld.setText("");
            c_psw_fld.setText("");
        }

