    public String Register_user_page(String username, String email_address, String phoneNumber, String userType, String password, String confirm_password) {
        try {
            // Check if any of the fields are empty
            if (username.isEmpty() || email_address.isEmpty() || userType.isEmpty() || password.isEmpty() || confirm_password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill all the fields.");
                return "empty fields";  // Return if any field is empty
            }

            // Validate userType
            if (!"admin".equalsIgnoreCase(userType) && !"employee".equalsIgnoreCase(userType)) {
                JOptionPane.showMessageDialog(null, "Invalid UserType! Please enter 'admin' or 'employee'.", "Error", JOptionPane.ERROR_MESSAGE);
                return "invalid usertype";  // Return invalid usertype
            }

            // Validate if passwords match
            if (!password.equals(confirm_password)) {
                // Show error message if passwords don't match
                JOptionPane.showMessageDialog(null, "Passwords do not match! Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
                return "password mismatch";  // Stop execution and return "password mismatch"
            }

            // Database connection
            Connection con = database.getConnection();
            Statement st = con.createStatement();

            // Construct SQL query to insert a new user into the database
            String query = "INSERT INTO register(user_name, email, user_type, password, confirm_password) "
                    + "VALUES ('" + username + "', '" + email_address + "', '" + userType + "', '" + password + "', '" + confirm_password + "')";

            // Execute the query
            st.execute(query);

            // Show success message and return success
            JOptionPane.showMessageDialog(null, "Successfully Added! Thank you.");
            return "success";

        } catch (SQLException | HeadlessException e) {
            // Handle SQL or GUI-related exceptions
            System.out.println("Error: " + e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            return "error";  // Return error if an exception occurs
        }
    }

